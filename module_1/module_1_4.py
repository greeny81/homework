my_string = input("Введите любой текст: ")

print("Количество символов введённого текста:", len(my_string))
print("Строка my_string в верхнем регистре:", my_string.upper())
print("Строка my_string в нижнем регистре:", my_string.lower())
print("Строка my_string без пробелов:", my_string.replace(' ',''))
print("Первый символ строки my_string.:", my_string[0])
print("Последний символ строки my_string.:", my_string[-1])

